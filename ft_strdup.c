#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dest;
	int		index;

	dest = malloc(sizeof(char) * ft_strlen(src));
	index = 0;
	while (src[index] != '\0')
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = '\0';
	return (dest);
}
